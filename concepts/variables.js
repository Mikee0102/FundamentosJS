// Declaración de variables con let y const
// var: en desuso

// Variables let: cambiarán de valor
let age; // declaración
console.log(age);
age = 23; //inicialización
console.log(age);

let name = "Juaquin"; // declaración e inicialización
console.log(name);
name = "Pancho";
console.log(name);

// constantes const: no cambiarán de valor
const PI = 3.1416;
// PI = 3.15;
console.log(PI);